/* |----------------------------------------------------------------------------------------------|
   |---------------------------Naive Solution (TC: O(N^3) | SC: O(1))-----------------------------|
   |----------------------------------------------------------------------------------------------| */

class Solution {
    public int subarraySum(int[] nums, int k) {
        
        int count = 0;
        
        for(int i = 0; i < nums.length; i++) {
            for(int j = i; j < nums.length; j++) {
            int temp = 0;
                for(int t = i; t <= j; t++) {
                    temp += nums[t];
                }
            if(temp == k)
                count++;
            }
        }
        return count;
    }
}


