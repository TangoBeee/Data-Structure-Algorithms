/* |----------------------------------------------------------------------------------------------|
   |--------------------------Naive Solution (TC: O(N^2) | SC: O(1))------------------------------|
   |----------------------------------------------------------------------------------------------| */

class Solution {
    public int reversePairs(int[] nums) {
        int pairs = 0;
        
        for(int i = 0; i < nums.length; i++) {
            for(int j = i+1; j < nums.length; j++) {
                if((long)nums[i] > (long)2*nums[j]) {
                    pairs++;
                }
            }
        }
        
        return pairs;
    }
}


