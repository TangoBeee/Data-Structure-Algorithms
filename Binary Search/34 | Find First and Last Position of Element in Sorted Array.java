/* |----------------------------------------------------------------------------------------------|
   |---------------------------Naive Solution (TC: O(N) | SC: O(1))-------------------------------|
   |----------------------------------------------------------------------------------------------| */

class Solution {
    public int[] searchRange(int[] nums, int target) {
        int start = -1;
        int end = -1;
        
        int i = 0, j = 0;
        
        while(i < nums.length && j < nums.length) {
            if(nums[i] == target) {
                start = i;
            }
            else
                i++;
            
            if(nums[j] == target) {
                end = j;
                j++;
            }
            else
                j++;
        }
        
        return new int[] {start, end};
    }
}



